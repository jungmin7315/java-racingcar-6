package model;

public class CarDTO {
    private String carname;
    private String count;


    public String getCarname() {
        return this.carname;
    }

    public void setCarname(String carname) {
        this.carname = carname;
    }

    public String getCount() {
        return this.count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}